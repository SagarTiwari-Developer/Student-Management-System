    public boolean isEmptyStudent() {
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Name Is Missing");
            return false;
        }
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student Date Of Birth Is Missing");
            return false;
        }
        if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "No Student From The Future");
            return false;
        }
        if (jTextField3.getText().matches("~^."
                + "\\00+$")) {
            JOptionPane.showMessageDialog(this, "Student E-Mail Is Missing Or Invalid");
            return false;
        }
        if (jTextField4.getText().length() >= 11) {
            JOptionPane.showMessageDialog(this, "Student Phone No. Is Missing Or Invalid");
            return false;
        }
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Father's Name Is Missing");
            return false;
        }
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Mother's Name Is Missing");
            return false;
        }
        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Address Line 01 Name Is Missing");
            return false;
        }
        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Address Line 02 Name Is Missing");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Please Add Your Image");
            return false;
        }

        return true;

    }


