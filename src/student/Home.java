    private void jButton37ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton37ActionPerformed
        // Course page Save Button:
        if (jTextField12.getText().isEmpty() || jComboBox2.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this, "Student ID Or Semester Number Is Missing ");
        } else {
            int id = course.getMax();
            int sid = Integer.parseInt(jTextField12.getText());
            int semesterNo = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            String course1 = jComboBox5.getSelectedItem().toString();
            String course2 = jComboBox6.getSelectedItem().toString();
            String course3 = jComboBox7.getSelectedItem().toString();
            String course4 = jComboBox8.getSelectedItem().toString();
            String course5 = jComboBox9.getSelectedItem().toString();
            if (course.isSemesterExist(sid, semesterNo)) {
                JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken Semester" + semesterNo);
            } else {
                if (course.isCourseExist(sid, "course1", course1)) {
                    JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken " + course1 + " course");
                } else {
                    if (course.isCourseExist(sid, "course2", course2)) {
                        JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken " + course2 + " course");
                    } else {
                        if (course.isCourseExist(sid, "course3", course3)) {
                            JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken " + course3 + " course");
                        } else {
                            if (course.isCourseExist(sid, "course4", course4)) {
                                JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken " + course4 + " course");
                            } else {
                                if (course.isCourseExist(sid, "course5", course5)) {
                                    JOptionPane.showMessageDialog(this, "This Student Has Alteady Taken " + course5 + " course");
                                } else {
                                    course.insert(id, sid, semesterNo, course1, course2, course3, course4, course5);
                                    jTable2.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester",
                                        "Course 1", "Course 2", "Course 3", "Course 4", "Course 5"}));
                                    course.getCourseValue(jTable2, "");
                                    clearCourse();

                                }
                            }
                        }
                    }
                }
            }

        }
    }//GEN-LAST:event_jButton37ActionPerformed

